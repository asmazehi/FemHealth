<?php

namespace App\Repository;

use App\Entity\Publication;
use Doctrine\Bundle\DoctrineBundle\Repository\ServiceEntityRepository;
use Doctrine\Persistence\ManagerRegistry;

/**
 * @extends ServiceEntityRepository<Publication>
 *
 * @method Publication|null find($id, $lockMode = null, $lockVersion = null)
 * @method Publication|null findOneBy(array $criteria, array $orderBy = null)
 * @method Publication[]    findAll()
 * @method Publication[]    findBy(array $criteria, array $orderBy = null, $limit = null, $offset = null)
 */
class PublicationRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Publication::class);
    }

    public function add(Publication $entity, bool $flush = false): void
    {
        $this->getEntityManager()->persist($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function remove(Publication $entity, bool $flush = false): void
    {
        $this->getEntityManager()->remove($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }
    public function countByCatLib($catlib)
    {
        return $this->createQueryBuilder('r')
            ->select('COUNT(r.catlib)')
            ->where('r.catlib = :catlib')
            ->setParameter('catlib', $catlib)
            ->getQuery()
            ->getSingleScalarResult();
    }
    public function findPublicationWithCommentCount()
    {
        return $this->createQueryBuilder('p')
            ->leftJoin('p.commentaires', 'c') // Supposons que 'comments' est le nom de la relation entre Publication et Comment
            ->select('p.id as publicationId, COUNT(c.id) as commentCount')
            ->groupBy('p.id')
            ->getQuery()
            ->getResult();
    }
